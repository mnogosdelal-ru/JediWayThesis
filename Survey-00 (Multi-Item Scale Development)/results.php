// Функция определения уровня по нормам
function getLevel($score, $scale) {
    // Нормы из публикации Мандриковой (усредненные)
    $norms = [
        'planomernost' => ['low' => 14, 'high' => 22],
        'celeustremlonnost' => ['low' => 30, 'high' => 38],
        'nastoichivost' => ['low' => 18, 'high' => 26],
        'fiksacia' => ['low' => 16, 'high' => 24],
        'samoorganizacia' => ['low' => 10, 'high' => 16],
        'orientacia_nastoyaschee' => ['low' => 7, 'high' => 11],
        'total' => ['low' => 100, 'high' => 130]
    ];
    
    if (!isset($norms[$scale])) return 'средний';
    
    if ($score < $norms[$scale]['low']) return 'низкий';
    if ($score > $norms[$scale]['high']) return 'высокий';
    return 'средний';
}

